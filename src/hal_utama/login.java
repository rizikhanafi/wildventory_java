public void login(){
        
        try {
        db.getkoneksi();
        Statement stat=(Statement) db.getkoneksi().createStatement();
        String sql="SELECT * FROM admin WHERE email='"+email.getText()+"' AND password='"+password.getText()+"'";
        ResultSet rs =stat.executeQuery(sql);
            if(rs.next()){
                if(email.getText().equals(rs.getString("email")) && password.getText().equals(rs.getString("password"))){
                    this.dispose();
                    new wildventory_admin.halaman_admin().setVisible(true);
                }
            }else {
                    JOptionPane.showMessageDialog(null, "Email atau Password salah.");
                    
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
